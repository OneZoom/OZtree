const webpack = require('webpack'); //to access built-in plugins
const CompressionPlugin = require('compression-webpack-plugin');
const HtmlWebpackPlugin = require('html-webpack-plugin');
const FileManagerPlugin = require('filemanager-webpack-plugin');
const TerserPlugin = require('terser-webpack-plugin');
const path = require('path')

function OZTreeModule_src(x) {
    return path.resolve(__dirname, 'OZprivate', 'rawJS', 'OZTreeModule', 'src', x);
}

function OZTreeModule_dist(x) {
    if (x) {
        return path.resolve(__dirname, 'OZprivate', 'rawJS', 'OZTreeModule', 'dist', x);
    } else {
        return path.resolve(__dirname, 'OZprivate', 'rawJS', 'OZTreeModule', 'dist');
    }
}

var config = {
  performance: {
    // See https://github.com/OneZoom/OZtree/issues/246
    maxEntrypointSize: 355000,
    maxAssetSize: 355000 
  },
  stats: {
    children: false
  },
  entry: {
    //at: './OZprivate/rawJS/OZTreeModule/src/at.js',
    OZentry: OZTreeModule_src('OZentry.js'),
    search_ui: OZTreeModule_src('search_ui.js'),
    leaf_draw: OZTreeModule_src('leaf_draw.js'),
  },
  optimization: {
    minimize: true,
    minimizer: [
      new TerserPlugin({
        test: /\.js(\?.*)?$/i,
      }),
    ],
  },
  output: {
    filename: '[name].[hash].js',
    path: OZTreeModule_dist(),
    //publicPath affects the script address in OZ_script.html generated by html webpack plugin
    publicPath: '/OZtree/static/OZTreeModule/dist/',
    library: '[name]'
  },
  externals: {
    'jquery': 'jQuery'
  },
  module: {
    rules: [
      {
        test: /\.js/,
        exclude: /(node_modules|bower_components)/,
        loader: 'babel-loader'
      }
    ]
  },
  plugins: [
    new HtmlWebpackPlugin({
      filename: 'OZ_main.html',
      // This template contains the script tags refering to the js files generated by
      // the webpack compile command
      template: OZTreeModule_src('OZ_script_template.html')
    }),
    new HtmlWebpackPlugin({
      filename: 'OZ_leaf_draw.html',
      chunks: ['leaf_draw'],
      // This template contains the script tags refering to the js files generated by
      // the webpack compile command, but only for the leaf_draw component
      template: OZTreeModule_src('OZ_script_template.html')
    }),
    new FileManagerPlugin({
      events: { onEnd: {
        copy: [
          {source: OZTreeModule_dist("OZentry.[hash].js"), destination: OZTreeModule_dist("OZentry.js")},
          {source: OZTreeModule_dist("search_ui.[hash].js"), destination: OZTreeModule_dist("search_ui.js")},
          {source: OZTreeModule_dist("leaf_draw.[hash].js"), destination: OZTreeModule_dist("leaf_draw.js")}
        ]
      }
    }}),
    new CompressionPlugin(
    )
  ]
}

module.exports = config;
